package basic.step02;

/* 
 * Operator - 연산자 (+, -, *, /)
 * Operand	- 피연산자 (a, b)
 */

public class Operator01 {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int result = a + b;
		System.out.println("a + b = " + result);
		
		int aa = 5;
		int bb = 2;
		float result2 = aa / bb;	// 5 / 2 = 2 -> float 2.0;
		
		System.out.println("aa / bb = " + result2);
		
	}

}






